package com.chekaz.data.structure;

public class StUser {
	//members	
		public  String 	USER_NO = null,
						COUNTRY = null,
						USER_NAME = null,	
						FULLNAME = null,
						DATE_ADDED = null;										
								
		public StUser(	String user_no,
						String country,
						String user_name,
						String fullname,
						String date_added																
						){		
			
						USER_NO = user_no;
						COUNTRY = country;
						USER_NAME = user_name;	
						FULLNAME = fullname;
						DATE_ADDED = date_added;
						
		}
}
